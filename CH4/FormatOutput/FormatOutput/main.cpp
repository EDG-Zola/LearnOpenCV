/************************************************************************/
/*            OpenCV中五种格式化输出方法                                */
/************************************************************************/

#include <iostream>
#include <opencv2/opencv.hpp>
using namespace std;
using namespace cv;
int main(){
	//Mat构造函数创建一个矩阵，并用randu函数填产生随机数填充矩阵，使用一个上下限来限制期望
	Mat r = Mat(10, 3, CV_8UC1);
	randu(r, Scalar::all(0), Scalar::all(255));
	/************************************************************************/
	/*                格式一：使用OpenCV默认风格                            */
	/************************************************************************/
	cout << "r (OpenCV默认风格) " << r << ";" << endl << endl;

	/************************************************************************/
	/*                格式二：使用Python风格                            */
	/************************************************************************/
	cout << "r (Python风格) " << format(r, "python") << ";" << endl << endl;

	/************************************************************************/
	/*                格式三：逗号分隔风格                            */
	/************************************************************************/
	cout << "r (逗号分隔分隔) " << format(r, "csv") << ";" << endl << endl;

	/************************************************************************/
	/*                格式四：Numpy风格                            */
	/************************************************************************/
	cout << "r (Numpy风格) " << format(r, "numpy") << ";" << endl << endl;

	/************************************************************************/
	/*                格式五：C语言风格                            */
	/************************************************************************/
	cout << "r (C语言风格) " << format(r, "C") << ";" << endl << endl;
}